public class OperadoresDeComparacao {
    public static void main(String[] args){
        boolean eMaior = 10 > 20; //falso
        boolean eMenor = 10 < 20; //verdadeiro
        boolean eIgual = 10 == 20; //falso
        boolean eMaiorIgual = 10 >= 20; //falso
        boolean eMenorIgual = 10 <= 20; //verdadeiro
        boolean eDiferente = 10 != 20; //verdadeiro

        System.out.println(eMaior);
        System.out.println(eMenor);
        System.out.println(eIgual);
        System.out.println(eMaiorIgual);
        System.out.println(eMenorIgual);
        System.out.println(eDiferente);
    }
}

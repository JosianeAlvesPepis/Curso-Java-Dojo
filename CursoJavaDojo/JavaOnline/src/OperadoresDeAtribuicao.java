/***
 * =, -=, +=, *=, /=, %=
 */

public class OperadoresDeAtribuicao {
    public static void main(String[] args) { //psvm+tab - atalho

        float salario = 1000;

        salario += 1000; //salario = salario + 1000
        salario -= 1000; //salario = salario - 1000
        salario *= 10; //salario = salario * 10
        salario /= 20; //salario = salario / 20
        salario %= 3; //salario = salario % 3

        System.out.println(salario); //sout+tab - atalho
    }
}
